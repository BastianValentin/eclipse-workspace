
public class Main {

	public static void main(String[] args) {
		
		//plus-equal (a = a + 10;)
		int a = 5;
		a+=10;
		System.out.println("a: " + a);
		
		//minus-equal (b = b - 10;)
		int b = 5;
		b-=10;
		System.out.println("b: " + b);
		
		//multiply-equal (c = c * 10;)
		int c = 5;
		c*=10;
		System.out.println("c: " + c);
		
		//divide-equal (d = d / 10;)
		double d = 5;
		d/=10;
		System.out.println("d: " + d);
		
		//modulus-equal (e = e % 10;)
		double e = 20;
		e%=8;
		System.out.println("e: " + e);

	}

}
